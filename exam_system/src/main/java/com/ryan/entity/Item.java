package com.ryan.entity;

public class Item {
	private int id;
	private String ino;
	private String item;
	private int istatus;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the ino
	 */
	public String getIno() {
		return ino;
	}
	/**
	 * @param ino the ino to set
	 */
	public void setIno(String ino) {
		this.ino = ino;
	}
	/**
	 * @return the item
	 */
	public String getItem() {
		return item;
	}
	/**
	 * @param item the item to set
	 */
	public void setItem(String item) {
		this.item = item;
	}
	/**
	 * @return the istatus
	 */
	public int getIstatus() {
		return istatus;
	}
	/**
	 * @param istatus the istatus to set
	 */
	public void setIstatus(int istatus) {
		this.istatus = istatus;
	}
	
}
